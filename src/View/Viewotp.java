    boolean kiemTraLoi() {
        if (txtOtp.getText().equalsIgnoreCase("")) {
            txtOtp.requestFocus();
            JOptionPane.showMessageDialog(this, "Không để trống OTP");
            return false;
        }
        if (txtMkmoi.getText().equalsIgnoreCase("")) {
            txtMkmoi.requestFocus();
            JOptionPane.showMessageDialog(this, "Không để trống mật khẩu");
            return false;
        }
        if (txtXacminh.getText().equalsIgnoreCase("")) {
            txtXacminh.requestFocus();
            JOptionPane.showMessageDialog(this, "Không để trống xác minh mật khẩu");
            return false;
        }       
        return true;
        
}
