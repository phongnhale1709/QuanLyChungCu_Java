    private void btnXacnhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacnhanActionPerformed
        if (kiemTraLoi()) {
            if (txtOtp.getText().equals(a) && kiemtrasl(txtMkmoi.getText().trim()) && txtMkmoi.getText().equals(txtMkmoi.getText())) {
                Daotaikhoan tkdao = new Daotaikhoan();
                Modeltaikhoan taikhoan = new Modeltaikhoan();
               
              

                try {
                    taikhoan = tkdao.getTaikhoanByEmail(email);
                } catch (SQLException ex) {
                    Logger.getLogger(Viewotp.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                taikhoan.setPassword(txtMkmoi.getText());     
                
                try {
                    tkdao.update(taikhoan);
                } catch (SQLException ex) {
                    Logger.getLogger(Viewotp.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ParseException ex) {
                    Logger.getLogger(Viewotp.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                JOptionPane.showMessageDialog(this, "Cập nhật mật khẩu thành công");
                Viewdangnhap dn = new Viewdangnhap();
                dn.show();
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Thông tin không chính xác");
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
        }
    }//GEN-LAST:event_btnXacnhanActionPerformed

