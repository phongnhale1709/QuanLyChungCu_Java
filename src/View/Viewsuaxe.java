    private void btnSuaxeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaxeActionPerformed
        Daoxe xe = new Daoxe();
        Modelxe s = new Modelxe();
        
        s.setMaXe( txtMaxe.getText());
        s.setBienSoXe(txtbsoxe.getText());
        s.setLoaiXe(txtloaixe.getText());
        s.setMaCD(txtmacd.getText());
  
             
        try {
            xe.update(s);
        } catch (SQLException ex) {
            Logger.getLogger(Viewsuaxe.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(Viewsuaxe.class.getName()).log(Level.SEVERE, null, ex);
        }
        new Viewxe().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnSuaxeActionPerformed

