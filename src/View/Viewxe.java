    private void btnxoaxeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaxeActionPerformed
        if (jTableXe.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn xe muốn xoá");
        } else {
            int output = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá xe", "Xác nhận xoá xe", JOptionPane.YES_NO_OPTION);
            if (output == JOptionPane.YES_OPTION) {
                Daoxe.getInstance().delete(getXESelect());
                JOptionPane.showMessageDialog(this, "Xóa thành công !");
                loadDataToTable();
            }
        }
    }//GEN-LAST:event_btnxoaxeActionPerformed

