     public void loadDataToTable() {
    try {
        Daoxe cddao = new Daoxe();
        ArrayList<MODEL.Modelxe> arms = cddao.selectAll();
        tblModel.setRowCount(0);
        for (MODEL.Modelxe i : arms ) {
            tblModel.addRow(new Object[]{
                    i.getMaXe(), i.getBienSoXe(),i.getLoaiXe(),i.getMaCD()           
            }
            );
        }
    } catch (Exception e) {
        System.err.println(e.toString());
        
    }
}
     public void loadDataToTableSearch(ArrayList<MODEL.Modelxe> result) {
        try {
            tblModel.setRowCount(0);
            for (MODEL.Modelxe i : result) {
                tblModel.addRow(new Object[]{
                   i.getMaXe(), i.getBienSoXe(),i.getLoaiXe(),i.getMaCD()          
                }
                );
            }
        } catch (Exception e) {  
        }
    }
 
