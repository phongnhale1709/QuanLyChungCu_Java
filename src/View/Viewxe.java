    public ArrayList<Modelxe> searchFn(String luaChon, String content) {
        ArrayList<MODEL.Modelxe> result = new ArrayList<>();
        Timxe timcd = new Timxe();
        switch (luaChon) {
            case "MAXE":
                result = timcd.searchMAXE(content);
                break;
            case "BIENSOXE":
                result = timcd.searchBSX(content);
                break;
            
        }
        return result;
    }
