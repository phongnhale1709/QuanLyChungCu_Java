    private void btnSuatkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuatkActionPerformed
        Daotaikhoan tk = new Daotaikhoan();
        Modeltaikhoan s = new Modeltaikhoan();
        s.setUsername(txtUser.getText());
        s.setPassword(txtPass.getText());
        s.setRole(txtRole.getText());
        s.setEmail(txtEmail.getText());
        
        try {
            tk.update(s);
        } catch (SQLException ex) {
            Logger.getLogger(Viewsuataikhoan.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(Viewsuataikhoan.class.getName()).log(Level.SEVERE, null, ex);
        }
        new Viewtaikhoan().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnSuatkActionPerformed

