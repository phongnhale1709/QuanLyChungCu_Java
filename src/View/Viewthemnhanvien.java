public String createId() {
    ArrayList<Modelnhanvien> nvAll = Daonhanvien.getInstance().selectAll();
    int maxId = 0;
    for (Modelnhanvien nv : nvAll) {
        String MANV = nv.getMANV();
        if (MANV.startsWith("NV")) {
            try {
                int id = Integer.parseInt(MANV.replaceAll("[^\\d]", ""));
                if (id > maxId) {
                    maxId = id;
                }
            } catch (NumberFormatException e) {
            }
        }
    }
    return  (String.format("NV%03d", maxId + 1));
}
